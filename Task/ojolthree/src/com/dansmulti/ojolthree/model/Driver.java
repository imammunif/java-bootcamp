package com.dansmulti.ojolthree.model;

public class Driver {
    private String name;
    private String platNo;

    public Driver(String name, String platNo) {
        this.name = name;
        this.platNo = platNo;
    }

    public String getName() {
        return name;
    }

    public String getPlatNo() {
        return platNo;
    }

}
